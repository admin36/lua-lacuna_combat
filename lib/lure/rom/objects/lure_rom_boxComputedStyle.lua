function lure.rom.newBoxComputedStyleObject()
	local self = {}
	
	--===================================================================
	-- OBJECT METATABLE                                                 =
	--===================================================================
	local self_mt = {}	
	---------------------------------------------------------------------	
	self_mt.__tostring = function(t)
		return "[object]:" .. tostring(self.nodeDesc)
	end
	---------------------------------------------------------------------	
	
	--===================================================================
	-- PROPERTIES                                                       =
	--===================================================================
	self.nodeDesc			= "ROMBoxComputedStyle"	
	---------------------------------------------------------------------	
	self.width 				= 0
	---------------------------------------------------------------------
	self.height 			= 0
	---------------------------------------------------------------------
	self.left 				= 0 
	---------------------------------------------------------------------
	self.top 				= 0
	---------------------------------------------------------------------
	self.padding 			= {0, 0, 0, 0}
	---------------------------------------------------------------------
	self.margin 			= {0, 0, 0, 0}
	---------------------------------------------------------------------
	self.backgroundColor 	= {255, 255, 255, 0}	
	---------------------------------------------------------------------
	self.borderWidth		= {0, 0, 0, 0}
	---------------------------------------------------------------------
	self.borderColor		= {"black", "black", "black", "black"}
	---------------------------------------------------------------------
	self.borderStyle		= "solid"	
	---------------------------------------------------------------------
	self.display			= "block"
	---------------------------------------------------------------------
	self.position			= "static"
	---------------------------------------------------------------------
	self.color				= {0, 0, 0}
	---------------------------------------------------------------------
	
	setmetatable(self, self_mt)	
	return self
end

