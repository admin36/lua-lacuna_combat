function lure.rom.newBoxRenderStyleObject()
	local self = {}
	
	--===================================================================
	-- OBJECT METATABLE                                                 =
	--===================================================================
	local self_mt = {}
	---------------------------------------------------------------------	
	self_mt.__tostring = function(t)
		return "[object]:" .. tostring(self.nodeDesc)
	end
	---------------------------------------------------------------------	
	
	--===================================================================
	-- PROPERTIES                                                       =
	--===================================================================
	self.nodeDesc			= "ROMBoxRenderStyle"	
	---------------------------------------------------------------------	
	self.width 				= nil
	---------------------------------------------------------------------
	self.height 			= nil
	---------------------------------------------------------------------
	self.left 				= 0 
	---------------------------------------------------------------------
	self.top 				= 0
	---------------------------------------------------------------------
	self.padding 			= {0, 0, 0, 0}
	---------------------------------------------------------------------
	self.margin 			= {0, 0, 0, 0}
	---------------------------------------------------------------------
	self.backgroundColor 	= nil
	---------------------------------------------------------------------
	self.borderWidth		= {0, 0, 0, 0}
	---------------------------------------------------------------------
	self.borderColor		= {"black", "black", "black", "black"}
	---------------------------------------------------------------------
	self.borderStyle		= "solid"
	---------------------------------------------------------------------
	self.borderColor		= "black"
	---------------------------------------------------------------------
	self.display			= "block"
	---------------------------------------------------------------------
	self.position			= "static"
	---------------------------------------------------------------------
	self.color				= "black"
	---------------------------------------------------------------------
	
	setmetatable(self, self_mt)	
	return self
end

