function lure.rom.newLayoutResponseObject()
	local self = {}
	
	--[[
		RESPONSE TYPES:
		1: layout is returning child nodes to parent
		2: UNUSED
		3: layout completed successfully	
	]]
	
	--===================================================================
	-- PROPERTIES                                                       =
	--===================================================================
	self.nodes	= {}
	---------------------------------------------------------------------
	self.type	= 3
	---------------------------------------------------------------------
	
	--====================================================================
	-- METHODS	                                                         =	
	--====================================================================
	self.clear = function()
		self.type 	= 3
		self.nodes 	= {}
	end
	---------------------------------------------------------------------
	
	return self
end