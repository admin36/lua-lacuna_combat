	---------------------------------------------------------------------
	self.nodeName 	= "SCRIPT"	
	---------------------------------------------------------------------
	self.nodeDesc	= "HTMLScriptElement"
	---------------------------------------------------------------------
	
	--===================================================================
	-- MUTATORS                                                         =
	--===================================================================
	
	--===================================================================
	-- METHODS	                                                        =	
	--===================================================================	
	
	return self
end